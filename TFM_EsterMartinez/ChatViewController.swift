import UIKit
import JSQMessagesViewController

struct User {
    let id: String
    let name: String
}

class ChatViewController: JSQMessagesViewController {

    let user1 = User(id: "1", name: "usuario1")
    let user2 = User(id: "2", name: "usuario2")
    
    var currentUser: User {
        return user1
    }
    
    // all messages 
    var messages = [JSQMessage] ()
}

extension ChatViewController {
    override func viewDidLoad() {
        super.viewDidLoad()
        
        self.senderId = currentUser.id
        self.senderDisplayName = currentUser.name
        
        self.messages = getMessages()
    }
    
    func getMessages() -> [JSQMessage] {
        var messages = [JSQMessage] ()
        
        let message1 = JSQMessage(senderId: "1", displayName: "usuario1", text: "Hola usuario 2")
        let message2 = JSQMessage(senderId: "2", displayName: "usuario2", text: "Hola usuario 1")
        
        messages.append(message1!)
        messages.append(message2!)
        
        return messages
    }
    
    override func collectionView(_ collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
        return messages.count
    }
    
    override func collectionView(_ collectionView: JSQMessagesCollectionView!, messageDataForItemAt indexPath: IndexPath!) -> JSQMessageData! {
        return messages[indexPath.item]
    }
    
    // Establecemos el color de los bubles
    override func collectionView(_ collectionView: JSQMessagesCollectionView!, messageBubbleImageDataForItemAt indexPath: IndexPath!) -> JSQMessageBubbleImageDataSource! {
        let bubbleFactory = JSQMessagesBubbleImageFactory()
        
        let message = messages[indexPath.row]
        
        if currentUser.id == message.senderId {
            return bubbleFactory?.outgoingMessagesBubbleImage(with: .blue)
        }
        else {
            return bubbleFactory?.incomingMessagesBubbleImage(with: .green)
        }
    }
    
    override func collectionView(_ collectionView: JSQMessagesCollectionView!, avatarImageDataForItemAt indexPath: IndexPath!) -> JSQMessageAvatarImageDataSource! {
        return nil
    }
    
    // Indicamos que usuario ha enviado cada mensaje
    override func collectionView(_ collectionView: JSQMessagesCollectionView!, attributedTextForMessageBubbleTopLabelAt indexPath: IndexPath!) -> NSAttributedString! {
        let message = messages[indexPath.row]
        let messageUsername = message.senderDisplayName
        return NSAttributedString(string: messageUsername!)
    }
    
    // Establecemos la altura para el nombre del usuario que envia el mensaje
    override func collectionView(_ collectionView: JSQMessagesCollectionView!, layout collectionViewLayout: JSQMessagesCollectionViewFlowLayout!, heightForMessageBubbleTopLabelAt indexPath: IndexPath!) -> CGFloat {
        return 1
    }
    
    override func didPressSend(_ button: UIButton!, withMessageText text: String!, senderId: String!, senderDisplayName: String!, date: Date!) {
        let message = JSQMessage(senderId: senderId, displayName: senderDisplayName, text: text)
        messages.append(message!)
        collectionView.reloadData()
        finishSendingMessage()
    }
}




